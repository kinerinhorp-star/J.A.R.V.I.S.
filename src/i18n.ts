export function useTranslation() {
  const [lang, setLang] = useState(localStorage.getItem('jarvis_lang') || 'Português');

  useEffect(() => {
    const handleSettingsChange = () => {
      setLang(localStorage.getItem('jarvis_lang') || 'Português');
    };
    window.addEventListener('jarvis_settings_updated', handleSettingsChange);
    return () => window.removeEventListener('jarvis_settings_updated', handleSettingsChange);
  }, []);

  const t = (key: keyof typeof translations['Português']) => {
    const languageDict = translations[lang as keyof typeof translations] || translations['Português'];
    return languageDict[key] || translations['Português'][key] || key;
  };

  return { t, lang };
}
